#Задача 1 Пользователь в бесконечном цикле вводит числа через консоль до тех пор,
# пока не введёт что-то, отличное от числа (после этого цикл завершается). В конце
# программа должна вывести сумму всех введённых чисел.

total = 0

while True:
    user_input = input("Введите число: ")

    if not user_input.isdigit():
        break
    total += int(user_input)

print("Сумма чисел: ",total)

#Задача 2 Пользователь вводит строку через консоль (состоящую только из букв в разном регистре).
# Программа должна создать новую строку, содержащую только те буквы из исходной строки,
# которые были в верхнем регистре.

text = input("Введите строку из букв: ")

result = ""

for char in text:
    if char.isupper():
        result += char

print("Буквы в верхнем регистре: ", result)

