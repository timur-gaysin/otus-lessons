#Задача 4 Пользователь вводит строку, содержащую любые символы (буквы, цифры и специальные символы). Программа должна вычислить и вывести сумму всех цифр, присутствующих в этой строке.

text = input("Введите строку, содержащую любые символы: ")

total = 0

for char in text:
    if char.isdigit():
        total += int(char)

print("Сумма всех цифр: ", total)

#Задача 5 Пользователь вводит через консоль 10 чисел. Программа должна определить максимальное число и вывести его в консоль.

max_number = None

print("Введите 10 цифр")
for i in range(10):
    number = int(input(f"Введите число {i+1}: "))
    if max_number is None or number > max_number:
        max_number = number

print("Максимальное число: ", max_number)
